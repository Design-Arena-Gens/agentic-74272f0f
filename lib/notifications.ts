import { format } from 'date-fns'

interface NotificationData {
  phoneNumber: string
  callerName?: string
  topic: string
  summary: string
  timestamp: string
}

export async function sendNotification(data: NotificationData): Promise<void> {
  const { phoneNumber, callerName, topic, summary, timestamp } = data

  const ownerWhatsApp = process.env.OWNER_WHATSAPP
  const ownerEmail = process.env.OWNER_EMAIL
  const ownerName = process.env.OWNER_NAME || 'Syed Eman Ali Shah'

  console.log('Sending notification for important call:', data)

  // Send WhatsApp notification
  if (ownerWhatsApp) {
    await sendWhatsAppMessage(ownerWhatsApp, {
      callerName: callerName || 'Unknown Caller',
      phoneNumber,
      topic,
      summary,
      timestamp
    })
  }

  // Send Email notification
  if (ownerEmail) {
    await sendEmailNotification(ownerEmail, {
      ownerName,
      callerName: callerName || 'Unknown Caller',
      phoneNumber,
      topic,
      summary,
      timestamp
    })
  }
}

async function sendWhatsAppMessage(
  recipientNumber: string,
  data: {
    callerName: string
    phoneNumber: string
    topic: string
    summary: string
    timestamp: string
  }
): Promise<void> {
  try {
    const accountSid = process.env.TWILIO_ACCOUNT_SID
    const authToken = process.env.TWILIO_AUTH_TOKEN
    const twilioWhatsApp = process.env.TWILIO_WHATSAPP_NUMBER || 'whatsapp:+14155238886'

    if (!accountSid || !authToken) {
      console.log('Twilio credentials not configured for WhatsApp')
      return
    }

    const message = `üîî *Important Call Alert*

üìû *Caller:* ${data.callerName}
üì± *Phone:* ${data.phoneNumber}
üìã *Topic:* ${data.topic}
üïê *Time:* ${format(new Date(data.timestamp), 'MMM d, yyyy ‚Ä¢ h:mm a')}

üí¨ *Summary:*
${data.summary}

---
Handled by Iqra AI Assistant`

    // In production, use Twilio WhatsApp API
    const client = require('twilio')(accountSid, authToken)

    await client.messages.create({
      from: twilioWhatsApp,
      to: `whatsapp:${recipientNumber}`,
      body: message
    })

    console.log('WhatsApp notification sent successfully')
  } catch (error) {
    console.error('Error sending WhatsApp message:', error)
  }
}

